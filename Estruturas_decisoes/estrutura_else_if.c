#include <stdio.h>

int main() {
    int maca = 74;
    int banana = 75;

    if (maca > banana) {
        printf("\nMaçã é maior que banana\n");
    } else if (maca < banana) {
        printf("\nMaçã é menor que banana\n");
    } else {
        printf("\nMaçã e banana são iguais!\n");
    }

    return 0;
}
